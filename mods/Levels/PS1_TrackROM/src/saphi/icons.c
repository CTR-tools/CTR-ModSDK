void DrawCustomIcon(unsigned iconID, Point pos, u_long* ot, unsigned transparency, int scale, Color color)
{
	PolyFT4 * p;
	GetPrimMem(p);
	if (p == nullptr) { return; }

	const PrimCode primCode = { .poly = { .renderCode = RenderCode_Polygon, .quad = 1, .textured = 1 } };
	color.code = primCode;
	p->colorCode = color;

    const RECT* iconPos = icons[iconID].image;
    const RECT* clutPos = icons[iconID].clut;
    const int ICON_WIDTH = iconPos->w * 4;
    const int ICON_HEIGHT = iconPos->h;
    const int BPP = 4;
    int u0 = (iconPos->x & 0x3F) * BPP;
    int u1 = u0 + ICON_WIDTH;
    int v0 = iconPos->y & 0xFF;
    int v1 = v0 + ICON_HEIGHT;

	int topX = pos.x;
	int bottomX = topX + FP_Mult(ICON_WIDTH, scale);
    int topY = pos.y;
    int bottomY = topY + FP_Mult(ICON_HEIGHT, scale);

	p->v[0].pos.x = topX;
	p->v[0].pos.y = topY;
	p->v[1].pos.x = bottomX;
	p->v[1].pos.y = topY;
	p->v[2].pos.x = topX;
	p->v[2].pos.y = bottomY;
	p->v[3].pos.x = bottomX;
	p->v[3].pos.y = bottomY;

	p->polyClut.x = clutPos->x / 16;
    p->polyClut.y = clutPos->y;
	p->polyTpage.x = iconPos->x / 64;
    p->polyTpage.y = iconPos->y / 256;
    p->polyTpage.texpageColors = 0;

	if (transparency)
	{
		p->polyTpage.semiTransparency = transparency - 1;
		p->colorCode.code.poly.semiTransparency = 1;
	}

	p->v[0].texCoords.u = u0;
	p->v[0].texCoords.v = v0;
	p->v[1].texCoords.u = u1;
	p->v[1].texCoords.v = v0;
	p->v[2].texCoords.u = u0;
	p->v[2].texCoords.v = v1;
	p->v[3].texCoords.u = u1;
	p->v[3].texCoords.v = v1;

	AddPrimitive(p, ot);
}

